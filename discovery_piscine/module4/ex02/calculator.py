#!/usr/bin/env python3

num1 = int(input("Give me first number: "))
num2 = int(input("Give me second number: "))
print("Thank you!")
print(f"{num1} + {num2} = {num1 + num2}")
print(f"{num1} - {num2} = {num1 - num2}")
print(f"{num1} / {num2} = {round(num1 / num2)}")
print(f"{num1} * {num2} = {num1 * num2}")